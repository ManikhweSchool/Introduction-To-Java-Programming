
public class Exercise_1_3b {

	// Student B also include the horizontal symmetry of the pattern.
	public static void main(String[] args) {
		
		System.out.println("    J     A     V     V     A");
		System.out.println("    J    A A     V   V     A A");
		System.out.println("J   J   AAAAA     V V     AAAAA");
		System.out.println(" J J   A     A     V     A     A");
			
		// To make the upside down version of the word java we change
		// the order in which we put our lines from bottom to top.
		System.out.println("\n J J   A     A     V     A     A");
		System.out.println("J   J   AAAAA     V V     AAAAA");
		System.out.println("    J    A A     V   V     A A");
		System.out.println("    J     A     V     V     A");
		
	}

}
